package es.ifp.programacion.uf1.condicional;


/**
 * Estructuras de seleccion condicional
 */

public class programaPrincipalCondicional 
{

	public static void main(String[] args) 
	{
		//Bloque de declaracion e inicializacion variables
		int numero;
		numero = 2;
		
		
		//Instrucciones
		//Transformacion
		
		if (numero>1) //Si la condicion es cierta, ejecuta estas lineas
		{ 
			//Salida de datos
			System.out.println("Verdadero");
		}
		
		else //Si la condicion no es cierta, se ejecutan estas otras
		{ 
			//Salida de datos
			System.out.println("Falso");
		}
		
	}

}
